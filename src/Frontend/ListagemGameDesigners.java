    public void carregarTabela(){
        modelo = (DefaultTableModel) Table.getModel();
        for(GameDesigner gd: lista.getListaGameDesigners()){
                modelo.addRow(new Object[]{gd.getNome(),gd.getTelefone(),gd.getNIF(),gd.getCache()});
        }
    }
    
    public void limparTabela(){
        modelo.setRowCount(0);
    }
