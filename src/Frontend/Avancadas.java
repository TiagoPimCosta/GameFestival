    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(sistema.getBilheteira().bilhetesVendidos() != 0){
            JOptionPane.showMessageDialog(null, "Já vendeu Bilhetes!");
        }else{
            sistema.getBilheteira().setPreco(Double.parseDouble(jTextField1.getText()));
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(Integer.parseInt(jTextField2.getText())<0){
            JOptionPane.showMessageDialog(null, "Os Colaborador não podem ganhar um valor negativo!");
        }else{
            sistema.getListaColaboradores().setSalario(Double.parseDouble(jTextField2.getText()));
            JOptionPane.showMessageDialog(null, "Salário alterado!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

